<div>
    <h1 class="bg-green-500 py-2 text-center font-bold font-serif uppercase text-md text-white">
        Pour compléter le formulaire de contact, suivez ces étapes simples :
    </h1>
    <div class="p-3">
        <p class="py-2 font-thin text-xs font-serif uppercase text-green-500">
            1. Nom et Prénom :
        </p>

        <p class="px-2 font-light text-sm">
            Dans les champs prévus à cet effet, saisissez votre nom de famille dans le champ "Nom" et votre prénom dans le champ "Prénom". Utilisez les mêmes noms que vous utilisez habituellement lors de vos communications.
        </p>
        <p class="py-2 font-thin text-xs font-serif uppercase text-green-500">
            2. Numéro de téléphone :
        </p>
        <p class="px-2 font-light text-sm">

            Dans le champ "Numéro de téléphone", saisissez votre numéro de téléphone actif. Assurez-vous de fournir un numéro où vous pouvez être facilement contacté.
        </p>
        <p class="py-2 font-thin text-xs font-serif uppercase text-green-500">
            3.Adresse e-mail :
        </p>
        <p class="px-2 font-light text-sm">

            Dans le champ "Adresse e-mail", entrez votre adresse e-mail principale. Veillez à utiliser une adresse e-mail valide et consultée régulièrement, car c'est l'endroit où vous recevrez la réponse à votre message.
        </p>
        <p class="py-2 font-thin text-xs font-serif uppercase text-green-500">
            4. Sélection du sujet du message :
        </p>
        <p class="px-2 font-light text-sm">

            Dans la liste déroulante ou le menu déroulant intitulé "Sujet du message" ou "Catégorie", choisissez l'option qui correspond le mieux à la raison de votre message. Par exemple, si vous avez une question sur un produit ou un service, sélectionnez l'option appropriée.
        </p>
        <p class="py-2 font-thin text-xs font-serif uppercase text-green-500">
            5. Zone de saisie pour le message :
        </p>
        <p class="px-2 font-light text-sm">

            Utilisez la zone de saisie fournie pour rédiger votre message. Expliquez clairement votre demande, votre question ou votre préoccupation. Plus vous fournissez de détails, plus il sera facile pour le destinataire de comprendre et de vous répondre adéquatement.
        </p>
        <p class="py-2 font-thin text-xs font-serif uppercase text-green-500">
            6. Bouton "Envoyer" :
        </p>
        <p class="px-2 font-light text-sm">

            Une fois que vous avez rempli tous les champs nécessaires et rédigé votre message, cliquez sur le bouton "Envoyer" pour soumettre le formulaire. Assurez-vous d'avoir bien vérifié toutes les informations que vous avez saisies avant de procéder à l'envoi.
        </p>
        <div class="flex justify-center p-5">
            <hr class="w-1/2 text-green-500 border border-green-500">
        </div>
        <p class="px-2 font-light text-sm text-center">
            Après avoir envoyé le formulaire, vous pouvez voir apparaître un message de confirmation à l'écran, vous indiquant que votre message a été envoyé avec succès. Vous devriez également recevoir une confirmation par e-mail vous informant que votre message a bien été reçu.
        </p>
       <p class="px-2 font-light text-sm text-center">

           En suivant ces étapes simples, vous pourrez compléter le formulaire de contact et envoyer votre message de manière efficace et précise.
       </p>


    </div>
</div>